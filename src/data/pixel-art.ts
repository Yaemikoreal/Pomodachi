/**
 * 创建表情覆盖层网格
 *
 * @param pixels - 像素数组 [row, col, color]
 * @returns 18x18 像素网格
 */
function createExpressionGrid(pixels: [number, number, string][]): PixelGrid {
  // 初始化空网格
  const grid: PixelGrid = Array.from({ length: 18 }, () => Array(18).fill(null))

  // 填充指定像素
  for (const [row, col, color] of pixels) {
    if (row >= 0 && row < 18 && col >= 0 && col < 18) {
      grid[row][col] = color
    }
  }

  return grid
}

/**
 * 合并基础图案和表情覆盖层
 *
 * @param base - 基础图案
 * @param overlay - 表情覆盖层（null 表示透明）
 * @returns 合并后的图案
 */
export function mergeGrids(base: PixelGrid, overlay: PixelGrid): PixelGrid {
  return base.map((row, y) =>
    row.map((baseColor, x) => {
      const overlayColor = overlay[y]?.[x]
      // 覆盖层非透明时使用覆盖层颜色
      return overlayColor !== null && overlayColor !== undefined
        ? overlayColor
        : baseColor
    })
  )
}
